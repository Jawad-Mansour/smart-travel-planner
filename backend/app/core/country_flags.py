"""Map English country names (and 2-letter codes) to regional-indicator flag emojis."""

from __future__ import annotations


def iso2_letters_to_flag(iso2: str) -> str:
    """Convert ISO 3166-1 alpha-2 (e.g. FR) to 🇫🇷."""
    code = (iso2 or "").strip().upper()
    if len(code) != 2 or not code.isalpha():
        return ""
    a, b = code
    return chr(ord(a) - ord("A") + 0x1F1E6) + chr(ord(b) - ord("A") + 0x1F1E6)


