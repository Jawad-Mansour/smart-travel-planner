def _normalize_country_key(name: str) -> str:
    s = (name or "").strip().lower()
    if s.startswith("the "):
        s = s[4:].strip()
    if "(" in s:
        s = s.split("(", 1)[0].strip()
    return s


def flag_emoji_for_country(country: str) -> str:
    """Return a flag emoji for a country name, or empty if unknown."""
    key = _normalize_country_key(country)
    if not key:
        return ""
    iso = _NAME_TO_ISO2.get(key)
    if iso:
        return iso2_letters_to_flag(iso)
    # Single-token match (e.g. "Thailand" already exact)
    for part in key.replace(",", " ").split():
        iso = _NAME_TO_ISO2.get(part.strip())
        if iso:
            return iso2_letters_to_flag(iso)
    return ""


def resolve_destination_flag(flag_emoji: str | None, country: str) -> str:
    """
    Prefer the model's flag when it looks real; otherwise derive from country.
    Accepts bare ISO2 (e.g. FR) as flag_emoji.
    """
    raw = (flag_emoji or "").strip()
    if raw and raw != "🌍":
        if len(raw) == 2 and raw.isalpha():
            conv = iso2_letters_to_flag(raw.upper())
            return conv or raw
        return raw
    resolved = flag_emoji_for_country(country)
    return resolved or "🌍"
